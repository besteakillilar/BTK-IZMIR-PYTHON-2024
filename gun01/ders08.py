# PYTHON MATH

print(60 * 12)  # * operatörü çarpma işlemi için kullanılır.
print(60 / 12)  # / operatörü bölme işlemi için kullanılır.
print(60 % 9)  # % operatörü mod almak için kullanılır.
print(60 // 7)  # // operatörü tam bölme için kullanılır.
print(60 - 7)  # - operatörü çıkarma işlemi için kullanılır.
print(60 ** 3)  # ** operatörü üs almak için kullanılır.
print(60 + 7)  # + operatörü toplama işlemi için kullanılır.
