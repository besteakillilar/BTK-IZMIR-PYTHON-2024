# PYTHON MATH

print(35 * 87)  # * operatörü sayılarda çarpma işlemini gerçekleştirir.
print(35 * 87.7)
print(35 * 87.0)

# / operatörü sayılarda bölme işlemini yapar ve sonucu her zaman float olur.
print(35 / 5)
print(35 / 5.0)
print(35 + 5.0)
print("35" + "90")  # + operatörü stringlerde birleştirme yapar.
print("beste" * 2)  # * operatörü stringlerde çoğaltma işlemi için kullanılır.
