# Python Comparison Operators

# Karşılaştırma operatörleri genelde sayılar üzerinde kullanılır.
# string ve boolean ifadelerinde eşitlik kontrolü amacı ile kullanılır.
# sonuçlar her zaman true veya false olur.

print(9 == 8)  # (a == b) a değeri b değerine eşit mi ?
print(9 != 8)  # (a != b) a değeri b değerine eşit değil mi ?

print(9 == 9)
print("ali" == "veli")
print("ali" != "veli")

print(True == True)
print(True == False)
print("8" == 8)
print("5 sayısı 8 den küçük mü ? ", 5 < 8)
print("5 sayısı 8 den küçük eşit mi ? ", 5 <= 8)
print("5 sayısı 8 den büyük eşit mi ? ", 5 >= 8)
print("5 sayısı 8 den büyük mü ? ", 5 > 8)

print("23 yaşında ehliyet alınabilir mi ? ", 23 >= 18)
