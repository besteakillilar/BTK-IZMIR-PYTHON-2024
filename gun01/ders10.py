# Python Logical Operators

# Karşıaştrıma işlemlerinin sonuçlarını bağlamak için kullanılır.
# Sonuçları true ya da false ile sonuçlanır.
# Ve için and, veya için or operatörü kullanılır.
# Not() değil amacı ile ya da tersine amacı ile kullanılır.

print((65 == 87) and (74 >= 45))
print((65 == 87) or (74 >= 45))
print(not (56 == 55))
