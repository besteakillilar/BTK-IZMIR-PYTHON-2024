# Python Assignment Operators

# sağdaki değeri veya işlem sonucunu soldaki değişkene değer olarak atar.

sayi = 10
print(sayi)
sayi = sayi + 5  # sayi += 5
print(sayi)

sayi -= 2  # iki çıkardık.
print(sayi)

sayi *= 3  # 3 ile çarptık.
print(sayi)

sayi //= 13  # 23 ile tam böldük.
print(sayi)

sayi /= 1  # 1 ile böldük.
print(sayi)

sayi **= 3  # 3 üssünü aldık.
print(sayi)

isim = "beste"
print(isim)
isim += "akıllılar"
isim *= 23
print(isim)
