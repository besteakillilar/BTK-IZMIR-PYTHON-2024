# gün değerine göre haftanın gün sayısı

gun = input("Gün değerini giriniz: ")
if gun == "Pazartesi":
    print("Birinci gün.")
elif gun == "Salı":
    print("İkinci gün.")
elif gun == "Çarşamba":
    print("Üçüncü gün.")
elif gun == "Perşembe":
    print("Dördüncü gün.")
elif gun == "Cuma":
    print("Beşinci gün.")
else:
    print("Hatalı gün girdiniz.")